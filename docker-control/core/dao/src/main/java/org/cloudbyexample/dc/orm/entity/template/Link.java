package org.cloudbyexample.dc.orm.entity.template;

import javax.persistence.Entity;

import org.springbyexample.contact.orm.entity.AbstractVersionedEntity;


/**
 * Link entity.
 *
 * @author David Winterfeldt
 */
@Entity
public class Link extends AbstractVersionedEntity {

    private static final long serialVersionUID = -7295708567157716571L;

    private String name = null;
    private String alias = null;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

}
